public class calcularArea{
	
	public static int areaCuadrado(int lado){
		return lado*lado;
	}
	public static int areaTriangulo (int altura, int lado){
		return lado*altura;
	}
	public static int areaRectangulo (int ladoCorto, int ladoLargo){
		return ladoCorto*ladoLargo;
	}
	public static double areaCirculo (int radio){
		return radio*3.14;
	}
}
